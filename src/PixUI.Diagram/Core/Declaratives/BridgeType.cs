namespace PixUI.Diagram;

/// <summary>
/// The types of bridges or crossings.
/// </summary>
public enum BridgeType
{
    /// <summary>
    /// No crossings will be displayed.
    /// </summary>
    None,

    /// <summary>
    /// The classic half-circle.
    /// </summary>
    Bow,

    /// <summary>
    /// A gap will display the intersection.
    /// </summary>
    Gap
}